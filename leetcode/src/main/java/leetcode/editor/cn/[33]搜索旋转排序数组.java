class Solution {
    public int search(int[] nums, int target) {

        // æ•°ç»„æ˜¯éƒ¨åˆ†æœ‰åºçš„ï¼Œå½“äºŒåˆ†åï¼Œå¿…å®šå­˜åœ¨ä¸€éƒ¨åˆ†æ˜¯æœ‰åºçš„
        // åˆ©ç”¨æœ‰åºçš„éƒ¨åˆ†è¿›è¡Œtargetæ¯”è¾ƒ

        if (nums == null) {
            return -1;
        }
        int len = nums.length;
        if (len == 0) {
            return -1;
        }

        if (len == 1) {
            return nums[0] == target ? 0 : -1;
        }

        int left = 0, right = len - 1;
        while (left <= right) {
            int mid = (left + right) >> 1;
            if (nums[mid] == target) {
                return mid;
            }
            if (nums[0] <= nums[mid]) {// è¡¨ç¤ºå‰åŠéƒ¨åˆ†æœ‰åº
                if (nums[left] <= target && target < nums[mid]) {
                    right = mid - 1;
                } else {
                    left = mid + 1;
                }
            } else {
                if (nums[mid] < target && target <= nums[len-1]) {
                    left = mid + 1;
                } else {
                    right = mid - 1;
                }
            }

        }
        return -1;
    }
}
